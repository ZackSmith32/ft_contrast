#include "libft.h"

void	ft_putarr(int *a, int len)
{
	int		i;

	i = 0;
	while (i < len)
	{
		ft_putnbr(a[i]);
		ft_putchar(' ');
		i++;
	}
	ft_putchar('\n');
}
