#include "libft.h"

int		ft_isnum(char *str)
{
	int		i;

	i = 0;
	if (str[i] == '-' || str[i] == '+')
		i++;
	if (i > 1)
		return (0);
	while (str[i] != 0)
	{
		if (!ft_isdigit(str[i]))
			return (0);
		i++;
	}
	if (--i < 12)
		return (1);
	return (0);
}
