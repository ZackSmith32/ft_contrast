#include "libft.h"

void	ft_freetab(char **tab)
{
	int		i;

	if (!tab)
		return ;
	i = 0;
	while (*tab != 0)
	{
		free(*tab);
		(tab)++;
		i++;
	}
	free(*tab);
}
